def revword(word:str):
    word= word.lower()[::-1]
    return word


def countword():
    counter=1
    lst=[]
    file= open("text.txt")
    for i in file:
        fixed_lst= i.rstrip().split()
        for j in fixed_lst:
            lst.append(j)
    word= lst[0]
    lst= lst[1:]
    for words in lst:
        x=revword(words)
        if x==word:
            counter= counter+1
    return counter
            

print("The number of rep is:", countword())   
    






